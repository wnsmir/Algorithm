a, b, c, d, e, f = map(int, input().split())
if a != 1:
    print(-(a - 1), end=" ")
else:
    print(0, end=" ")

if b != 1:
    print(-(b - 1), end=" ")
else:
    print(0, end=" ")

if c != 2:
    print(-(c - 2), end=" ")
else:
    print(0, end=" ")

if d != 2:
    print(-(d - 2), end=" ")
else:
    print(0, end=" ")

if e != 2:
    print(-(e - 2), end=" ")
else:
    print(0, end=" ")

if f != 8:
    print(-(f - 8), end=" ")
else:
    print(0, end=" ")